package Tareas;

public class Persona {
	
private String nombre, puesto;
	
	private double horas, tiempoExtra;

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public String getPuesto() {
		return puesto;
	}

	public void setPuesto(String puesto) {
		this.puesto = puesto;
	}

	public double getHoras() {
		return horas;
	}

	public void setHoras(double horas) {
		this.horas = horas;
	}

	public double getTiempoExtra() {
		return tiempoExtra;
	}

	public void setTiempoExtra(double tiempoExtra) {
		this.tiempoExtra = tiempoExtra;
	}
	

}
